// this function takes two number and returns the sum of them
pub fn add(a: i32, b: i32) -> i32 {
    a + b
}

// subtract function
pub fn subtract(a: i32, b: i32) -> i32 {
    a - b
}

// multiply function
pub fn multiply(a: i32, b: i32) -> i32 {
    a * b
}

// divide function
pub fn divide(a: i32, b: i32) -> i32 {
    a / b
}
